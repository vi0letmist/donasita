            <h2><strong>Membangun Sekolah, Membangun Bangsa</strong></h2>

            <p>Halo sobat, semoga berbahagia. Tak terasa sudah hampir 3 tahun sekolah ini berdiri. Suka duka ternyata jadi modal kami berbahagia. Gedung sekolah telah berdiri, meski sederhana tapi cukup melegakan kala harus berlindung dari panas dan hujan.<br />
            <br />
            Sobat, pembangunan sekolah Cerdik Cendekia bukan hanya soal gedung dan buku-buku. Manusia belajar dari hidupnya. Oleh karena itu kami selalu berusaha membawa adik-adik melihat khazanah baru dari hidupnya.<br />
            <br />
            Tempo hari datang seorang bapak yang ujuk-ujuk menawari tanah di sebelah sekolah agar bisa digarap oleh sekolah. Tentu kami tak menyia nyiakan kesempatan ini. Adik adik segera kami ajak berkebun dan beternak.<br />
            <br />
            Tak lama berselang datang lagi seorang pengusaha yang menawari diri untuk mengajar. Tak tanggung ia juga memberikan modal untuk ternak belut, ikan cupang, dan beberapa ikan hias lain.<br />
            <br />
            Kami juga masih ingat ketika sepasang Suami Istri dengan suka rela mengajari kami membuat film. Adik-adik teater pun dengan senang hati menyambutnya.<br />
            <br />
            Ada juga sepasang suami istri yang sudah sepuh sering datang ke sekolah. Lemari buku di sekolah, itu mereka yang belikan. Tentu juga 2 kali perjalanan studi wisata ke Cariu dan Taman Mini Indonesia Indah bisa terlaksana karena dukungan mereka.<br />
            <br />
            Buku-buku bacaan di sekolah, gedung yang berdiri, dan masih banyak lagi juga karena teman-teman di luar sana, dan kamu yang tak henti terus memberi dukungan. Ah masih banyak lagi pahlawan sekolah kami yang bahkan tak pernah kami tatap. Sobat, seandainya kamu datang ke Sekolah kami, rasanya ingin kami memelukmu.<br />
            Belajar memang tak melulu mesti di kelas. Peng-alam-an adalah guru terbaik, diri pribadi ia guru sejati, alam raya telah menjadi laboratorium terbesar kami.<br />
            <br />
            Sobat, kami ingin menjadi sekolah yang mandiri. Kolam kolam ikan yang sekarang kami kerjakan adalah impian yang kami lambungkan dalam doa kala sekolah ini di dirikan. Kami ingin sekolah ini tak hanya mendidik anak manusia, tapi juga menyejahterakan.</p>

            <p>Sobat, angkatan pertama sekolah kami akan segera lulus. Adik-adik mulai kasak-kusuk bertanya, apakah SMA Cerdik Cendekia juga akan di dirikan? Para guru pun mulai membicarakannya. Kami lalu menepi di malam sunyi sendiri sendiri, memanjatkan doa agar benar SMA Cerdik Cendekia&nbsp;bisa kami bangun.<br />
            <br />
            Padahal tak melulu muluk. Melihat teman-teman guru yang tahan hingga hari ini mengajar tanpa diberi upah saja sudah kami syukuri.<br />
            <br />
            Tapi sobat, jikalau boleh kami menjadi sekolah yang mandiri. Kami ucapkan terimakasih padamu yang terus mendukung terwujudnya cita-cita kami. Terimakasih sobat. Semoga engkau senantiasa bahagia.<br />
            <br />
            Sabtu,&nbsp;28 Desember 2019</p>

